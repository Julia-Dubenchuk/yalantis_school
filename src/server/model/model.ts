import mongoose from 'mongoose';

const schema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
});

const Userdb = mongoose.model('userdb', schema);

export default Userdb;
